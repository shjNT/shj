#include<iostream>
using namespace std;

//转义字符


int main(){
    //①换行符： \n
    cout << "hello \n world" << endl;

    //②反斜杠： 
    cout << "\\" << endl;

    // ③水平制表符： \t
    cout << "aaa \t helloworld" << endl;
    cout << "aaa111 \t helloworld" << endl;
    cout << "aaabbcccc \t helloworld" << endl;
    return 0;
}